function showPizzaDetails() {
    var pizzaDetails = document.getElementById('pizzaDetails');
    if (pizzaDetails.style.display === 'none') {
        pizzaDetails.style.display = 'block';
    } else {
        pizzaDetails.style.display = 'none';
    }
}

